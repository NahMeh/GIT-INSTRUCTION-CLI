def clear_screen():
    os.system("cls" if os.name == "nt" else "clear")

def show_banner():
    print("\033[1;36m" + banner + "\033[0m\n")

def main_menu():
    while True:
        clear_screen()
        show_banner()
        print("1. How to setup VS Code")
        print("2. How to setup Git")
        print("3. How to setup GitHub Repository")
        print("4. How to connect Git to GitHub Repository")
        print("0. Exit\n")

        choice = input("\033[1;33mEnter your choice: \033[0m")
        clear_screen()

        if choice == "1":
            vscode_setup()
        elif choice == "2":
            git_setup()
        elif choice == "3":
            github_repo_setup()
        elif choice == "4":
            connect_git_to_github()
        elif choice == "0":
            print("\033[1;31mExiting... Goodbye!\033[0m\n")
            time.sleep(1)
            break
        else:
            print("\033[1;31mInvalid choice. Please try again.\033[0m")
            time.sleep(1)

def navigate(current):
    print("\n\033[1;34mOptions:\033[0m")
    print("[N] Next | [P] Previous | [M] Main Menu | [E] Exit")
    nav = input("â†’ ").strip().lower()

    if nav == "n":
        return current + 1
    elif nav == "p":
        return current - 1
    elif nav == "m":
        return 0
    elif nav == "e":
        clear_screen()
        print("\033[1;31mExiting... Goodbye!\033[0m\n")
        time.sleep(1)
        exit()
    else:
        print("\033[1;31mInvalid option, returning to menu...\033[0m")
        time.sleep(1)
        return 0

def vscode_setup():
    print("\033[1;32m[VS Code Setup]\033[0m\n")
    print("Go to https://code.visualstudio.com")
    print("Click 'Download' and select your operating system.")
    print("Setup your Vscode according to yout preferences")
    print("To confirm the installation for windows, press  Win + R and put 'cmd' to run windows terminal and write 'code', and vscode should open. Its the same for Linux, just go to your respective terminal and write 'code'. It  should run vscode in your terminal. (Make sure the command 'code' is installed and in your PATH)")
    next_action = navigate(1)
    route(next_action)

def git_setup():
    print("\033[1;32m[Git Setup]\033[0m\n")
    print("Go to https://git-scm.com/install/windows")
    print("Select your operating system.")
    print("You should see  'Click here to download'. Click it  to get the latest version of Git.")
    print("Setup your Git according to your preference")
    print("To confirm git was installed, go to your respective terminal and type 'git'.\n")
    next_action = navigate(2)
    route(next_action)

def github_repo_setup():
    print("\033[1;32m[GitHub Repository Setup]\033[0m\n")
    print("Go to https://github.com")
    print("Log in to your account. (Or sign in if you dont have one already)")
    print("Once logged in, click the '+' icon at the top right and choose 'New Repository'.")
    print("Enter your repository name")
    print("Choose visibility. (Public = Anyone can see your repo. Private = Only you and invited person can access it")
    print("Click 'Create repository'.\n")
    next_action = navigate(3)
    route(next_action)

def connect_git_to_github():
    print("\033[1;32m[Connecting Git to GitHub Repository]\033[0m\n")
    print("Go to your terminal")
    print("Enter these one by one in your terminal:\n")
    print("git config --global user.name \"Your Name\"")
    print("git config --global user.email \"you@youraddress.com\"")
    print("mkdir 'folder name'.")
    print("cd 'folder name you just made'.\n")
    print("git init")
    print("To add your project files = git add .")
    print("To commit your file = git commit -m 'initial commit'.")
    print("To connect your local folder to your github repository = git remote add origin 'your repository URL'")
    print("To upload youe local files to github =  git push -u origin main\n")
    next_action = navigate(4)
    route(next_action)

def route(step):
    clear_screen()
    if step == 1:
        vscode_setup()
    elif step == 2:
        git_setup()
    elif step == 3:
        github_repo_setup()
    elif step == 4:
        connect_git_to_github()
    else:
        main_menu()

if __name__ == "__main__":
    main_menu()
